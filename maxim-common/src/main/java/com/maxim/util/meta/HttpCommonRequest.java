package com.maxim.util.meta;

import java.util.Map;

public class HttpCommonRequest {

    private String url;
    private Map<String, Object> params;

    public HttpCommonRequest() {
    }

    public HttpCommonRequest(String url) {
        this.url = url;
    }

    public HttpCommonRequest(String url, Map<String, Object> jsonParams) {
        this.url = url;
        this.params = jsonParams;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public Map<String, Object> getParams() {
        return params;
    }

    public void setParams(Map<String, Object> params) {
        this.params = params;
    }

}